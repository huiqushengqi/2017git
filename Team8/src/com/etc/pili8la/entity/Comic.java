package com.etc.pili8la.entity;

public class Comic {
  private int comic_id;
  private String comic_cover;
  private String comic_title;
  private String comic_author;
  private int comic_type;
  private String comic_link;
public int getComic_id() {
	return comic_id;
}
public void setComic_id(int comic_id) {
	this.comic_id = comic_id;
}
public String getComic_cover() {
	return comic_cover;
}
public void setComic_cover(String comic_cover) {
	this.comic_cover = comic_cover;
}
public String getComic_title() {
	return comic_title;
}
public void setComic_title(String comic_title) {
	this.comic_title = comic_title;
}
public String getComic_author() {
	return comic_author;
}
public void setComic_author(String comic_author) {
	this.comic_author = comic_author;
}
public int getComic_type() {
	return comic_type;
}
public void setComic_type(int comic_type) {
	this.comic_type = comic_type;
}
public String getComic_link() {
	return comic_link;
}
public void setComic_link(String comic_link) {
	this.comic_link = comic_link;
}
  
}
