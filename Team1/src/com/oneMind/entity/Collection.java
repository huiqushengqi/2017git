package com.oneMind.entity;

public class Collection {
	private int collection_id;
	private int collection_student;
	private int collection_post;
	
	public int getCollection_id() {
		return collection_id;
	}
	public void setCollection_id(int collection_id) {
		this.collection_id = collection_id;
	}
	public int getCollection_student() {
		return collection_student;
	}
	public void setCollection_student(int collection_student) {
		this.collection_student = collection_student;
	}
	public int getCollection_post() {
		return collection_post;
	}
	public void setCollection_post(int collection_post) {
		this.collection_post = collection_post;
	}

}
