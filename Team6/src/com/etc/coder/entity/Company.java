package com.etc.coder.entity;

public class Company {
	private int company_id;//企业id
	private String company_name;//企业名字
	private String company_loginName;//企业登录名
	private String company_password;//企业密码
	private String company_attibute;//企业性质
	private String company_scale;//企业规模
	private String company_address;//企业地址
	private String company_email;//企业邮箱
	private String company_tel;//企业电话
	private String company_info;//企业简介
	private String company_photo;//企业图标
	
	
	
    
	public Company() {
	}
	
	public Company(int companyId, String companyName, String companyLoginName,
			String companyPassword, String companyAttibute,
			String companyScale, String companyAddress, String companyEmail,
			String companyTel, String companyInfo, String companyPhoto) {
		company_id = companyId;
		company_name = companyName;
		company_loginName = companyLoginName;
		company_password = companyPassword;
		company_attibute = companyAttibute;
		company_scale = companyScale;
		company_address = companyAddress;
		company_email = companyEmail;
		company_tel = companyTel;
		company_info = companyInfo;
		company_photo = companyPhoto;
	}

	public int getCompany_id() {
		return company_id;
	}
	public void setCompany_id(int companyId) {
		company_id = companyId;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String companyName) {
		company_name = companyName;
	}
	public String getCompany_password() {
		return company_password;
	}
	public void setCompany_password(String companyPassword) {
		company_password = companyPassword;
	}
	public String getCompany_attibute() {
		return company_attibute;
	}
	public void setCompany_attibute(String companyAttibute) {
		company_attibute = companyAttibute;
	}
	public String getCompany_scale() {
		return company_scale;
	}
	public void setCompany_scale(String companyScale) {
		company_scale = companyScale;
	}
	public String getCompany_address() {
		return company_address;
	}
	public void setCompany_address(String companyAddress) {
		company_address = companyAddress;
	}
	public String getCompany_email() {
		return company_email;
	}
	public void setCompany_email(String companyEmail) {
		company_email = companyEmail;
	}
	public String getCompany_tel() {
		return company_tel;
	}
	public void setCompany_tel(String companyTel) {
		company_tel = companyTel;
	}
	public String getCompany_info() {
		return company_info;
	}
	public void setCompany_info(String companyInfo) {
		company_info = companyInfo;
	}
	public String getCompany_photo() {
		return company_photo;
	}
	public void setCompany_photo(String companyPhoto) {
		company_photo = companyPhoto;
	}
	public String getCompany_loginName() {
		return company_loginName;
	}
	public void setCompany_loginName(String company_loginName) {
		this.company_loginName = company_loginName;
	}
	
	
	
	
}
