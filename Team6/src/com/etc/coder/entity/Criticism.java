package com.etc.coder.entity;

public class Criticism {
	private int criticism_id;//评论id
	private Message cri_message;//留言类的对象，外键（Message的主键）
	private User cri_user;//评论的用户
	private String criticism_text;//评论内容
	private String criticism_date;//评论时间
	
	
	
	public User getCri_user() {
		return cri_user;
	}
	public void setCri_user(User cri_user) {
		this.cri_user = cri_user;
	}
	public int getCriticism_id() {
		return criticism_id;
	}
	public void setCriticism_id(int criticismId) {
		criticism_id = criticismId;
	}
	public Message getCri_message() {
		return cri_message;
	}
	public void setCri_message(Message criMessage) {
		cri_message = criMessage;
	}
	public String getCriticism_text() {
		return criticism_text;
	}
	public void setCriticism_text(String criticismText) {
		criticism_text = criticismText;
	}
	public String getCriticism_date() {
		return criticism_date;
	}
	public void setCriticism_date(String criticismDate) {
		criticism_date = criticismDate;
	}
	
}
