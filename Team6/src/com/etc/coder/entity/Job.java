package com.etc.coder.entity;

public class Job {
	private int job_id;//岗位id
	private Technolgytype job_type;//职位类别
	private String job_name;//岗位名称
	private String job_description;//岗位描述
	private String job_address;//岗位工作地点
	private String job_salary;//岗位工资
	private String job_education;//岗位的学历要求
	private String job_exp;//岗位的工作经验
	private String job_date;//岗位发布时间
	private int job_issue; //岗位是否通过审核，1表示审核通过，0表示还没审核
	public int getJob_issue() {
		return job_issue;
	}
	public void setJob_issue(int job_issue) {
		this.job_issue = job_issue;
	}
	public int getJob_id() {
		return job_id;
	}
	public void setJob_id(int jobId) {
		job_id = jobId;
	}
	public String getJob_name() {
		return job_name;
	}
	public void setJob_name(String jobName) {
		job_name = jobName;
	}
	public Technolgytype getJob_type() {
		return job_type;
	}
	public void setJob_type(Technolgytype jobType) {
		job_type = jobType;
	}
	public String getJob_description() {
		return job_description;
	}
	public void setJob_description(String jobDescription) {
		job_description = jobDescription;
	}
	public String getJob_address() {
		return job_address;
	}
	public void setJob_address(String jobAddress) {
		job_address = jobAddress;
	}
	public String getJob_salary() {
		return job_salary;
	}
	public void setJob_salary(String jobSalary) {
		job_salary = jobSalary;
	}
	public String getJob_education() {
		return job_education;
	}
	public void setJob_education(String jobEducation) {
		job_education = jobEducation;
	}
	public String getJob_exp() {
		return job_exp;
	}
	public void setJob_exp(String jobExp) {
		job_exp = jobExp;
	}
	public String getJob_date() {
		return job_date;
	}
	public void setJob_date(String jobDate) {
		job_date = jobDate;
	}
	
}
